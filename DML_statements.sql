INSERT INTO ActionTable (rule_id, device_id, action_type, parameters)
SELECT 
    r.rule_id,
    d.device_id,
    CASE 
        WHEN c.attribute = 'smoke' THEN 'send_alert'
        WHEN c.attribute = 'gas' THEN 'turn_off_gas'
        WHEN c.attribute = 'water_leak' THEN 'shut_off_water'
        WHEN c.attribute = 'motion' THEN 'turn_on_light'
        WHEN c.attribute = 'temperature' THEN 'turn_on_heater'
        WHEN c.attribute = 'power_consumption' THEN 'turn_off_devices'
    END AS action_type,
    CASE 
        WHEN c.attribute = 'smoke' THEN 'Smoke detected'
        WHEN c.attribute = 'gas' THEN 'Gas leak detected'
        WHEN c.attribute = 'water_leak' THEN 'Water leak detected'
        WHEN c.attribute = 'motion' THEN 'Auto light activation'
        WHEN c.attribute = 'temperature' THEN 'Heating activated'
        WHEN c.attribute = 'power_consumption' THEN 'Reducing power usage'
    END AS parameters
FROM Rules r
JOIN ConditionTable c ON r.rule_id = c.rule_id
JOIN Room ro ON r.home_id = ro.home_id
JOIN Device d ON ro.room_id = d.room_id
WHERE d.device_id = (
    SELECT TOP 1 d2.device_id
    FROM Device d2
    JOIN Room r2 ON d2.room_id = r2.room_id
    WHERE r2.home_id = r.home_id
    ORDER BY NEWID()
);
GO


INSERT INTO ExecutionLog (rule_id, event_id, action_id, execution_time, status, error_message)
SELECT 
    a.rule_id,
    e.event_id,
    a.action_id,
    DATEADD(MINUTE, ABS(CHECKSUM(NEWID())) % 120, e.[timestamp]) AS execution_time,

    CASE 
        WHEN ABS(CHECKSUM(NEWID())) % 10 < 8 THEN 'success'
        ELSE 'failed'
    END AS status,

    CASE 
        WHEN ABS(CHECKSUM(NEWID())) % 10 < 8 THEN NULL
        ELSE 
            CASE ABS(CHECKSUM(NEWID())) % 3
                WHEN 0 THEN 'Device offline'
                WHEN 1 THEN 'Timeout error'
                ELSE 'Connection lost'
            END
    END AS error_message

FROM ActionTable a
JOIN Rules r ON a.rule_id = r.rule_id
JOIN Event e ON e.sensor_id IN (
    SELECT s.sensor_id
    FROM Sensor s
    JOIN Device d ON s.device_id = d.device_id
    JOIN Room ro ON d.room_id = ro.room_id
    WHERE ro.home_id = r.home_id
)
WHERE e.event_id IN (
    SELECT TOP 2 e2.event_id
    FROM Event e2
    ORDER BY NEWID()
);
GO




