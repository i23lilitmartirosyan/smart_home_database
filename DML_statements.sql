INSERT INTO Sensor (device_id, sensor_type, unit, last_seen)
SELECT
    d.device_id,
    CASE
        WHEN d.device_type = 'Smoke Detector' THEN 'Smoke'
        WHEN d.device_type = 'Gas Sensor' THEN 'Gas'
        WHEN d.device_type IN ('Thermostat', 'Heater Controller') THEN 'Temperature'
        WHEN d.device_type IN ('Motion Sensor', 'Security Camera') THEN 'Motion'
        WHEN d.device_type = 'Water Leak Sensor' THEN 'Water Leak'
        WHEN d.device_type = 'Smart Plug' THEN 'Power Consumption'
    END AS sensor_type,
    CASE
        WHEN d.device_type IN ('Smoke Detector', 'Gas Sensor') THEN 'ppm'
        WHEN d.device_type IN ('Thermostat', 'Heater Controller') THEN 'Celsius'
        WHEN d.device_type IN ('Motion Sensor', 'Security Camera', 'Water Leak Sensor') THEN 'boolean'
        WHEN d.device_type = 'Smart Plug' THEN 'W'
    END AS unit,
    DATEADD(MINUTE, -ABS(CHECKSUM(NEWID())) % 10000, GETDATE()) AS last_seen
FROM Device d
WHERE d.device_type IN (
    'Smoke Detector',
    'Gas Sensor',
    'Thermostat',
    'Heater Controller',
    'Motion Sensor',
    'Security Camera',
    'Water Leak Sensor',
    'Smart Plug'
);
GO





INSERT INTO Event (sensor_id, event_type, value, [timestamp], source)
SELECT
    s.sensor_id,
    s.sensor_type,
    CASE
        WHEN s.sensor_type = 'Temperature' THEN CAST(CAST(18 + RAND(CHECKSUM(NEWID())) * 8 AS DECIMAL(4,1)) AS VARCHAR(20))
        WHEN s.sensor_type = 'Motion' THEN CAST(CAST(RAND(CHECKSUM(NEWID())) * 2 AS INT) AS VARCHAR(20))
        WHEN s.sensor_type = 'Smoke' THEN CAST(CAST(5 + RAND(CHECKSUM(NEWID())) * 120 AS INT) AS VARCHAR(20))
        WHEN s.sensor_type = 'Gas' THEN CAST(CAST(10 + RAND(CHECKSUM(NEWID())) * 250 AS INT) AS VARCHAR(20))
        WHEN s.sensor_type = 'Power Consumption' THEN CAST(CAST(30 + RAND(CHECKSUM(NEWID())) * 180 AS INT) AS VARCHAR(20))
        WHEN s.sensor_type = 'Water Leak' THEN CAST(CAST(RAND(CHECKSUM(NEWID())) * 2 AS INT) AS VARCHAR(20))
    END,
    DATEADD(MINUTE, -ABS(CHECKSUM(NEWID())) % 4320, GETDATE()),
    'sensor'
FROM Sensor s;
